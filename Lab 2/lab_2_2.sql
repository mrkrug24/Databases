--------------------------------------------------------------------------------------------------------------------------------

-- Создание таблицы команд
CREATE TABLE IF NOT EXISTS team (
    id SERIAL PRIMARY KEY,
    team_name VARCHAR(20) NOT NULL,
    country VARCHAR(20) NOT NULL,
    car_model VARCHAR(5) NOT NULL,
    UNIQUE (team_name)
);

-- Создание таблицы пилотов
CREATE TABLE IF NOT EXISTS driver (
    id INT PRIMARY KEY,     -- уникальный код на всю спортивную карьеру (с 2014 года)
    team_id INT NOT NULL,
    first_name VARCHAR(20) NOT NULL,
    last_name VARCHAR(20) NOT NULL,
    birthday DATE CONSTRAINT majority_age CHECK (birthday <= (CURRENT_DATE - INTERVAL '18 years')) NOT NULL,
    citizenship VARCHAR(20) DEFAULT 'Unknown',
    growth INT,
    weight INT,
    FOREIGN KEY (team_id) REFERENCES team(id) ON DELETE CASCADE
);

-- Тип данных для race_status
CREATE TYPE RS AS ENUM ('passed', 'canceled', 'upcoming');

-- Создание таблицы гонок
CREATE TABLE IF NOT EXISTS race (
    id INT PRIMARY KEY,
    race_status RS,
    date DATE NOT NULL,
    laps INT CONSTRAINT positive_num_laps CHECK (laps > 0) NOT NULL
);

-- Создание таблицы трасс
CREATE TABLE IF NOT EXISTS track (
    race_id INT,
    city VARCHAR(20) NOT NULL,
    length INT CONSTRAINT positive_length CHECK (length > 0),
    UNIQUE (city),
    PRIMARY KEY (race_id),
    FOREIGN KEY (race_id) REFERENCES race(id) ON DELETE CASCADE
);

-- Тип данных для driver_status
CREATE TYPE DS AS ENUM ('finished', 'missed', 'breakdown', 'accident', 'disqualified', 'upcoming');

-- Создание таблицы результатов
CREATE TABLE IF NOT EXISTS result (
    driver_id INT,
    race_id INT,
    driver_status DS,
    points INT CONSTRAINT positive_num_points CHECK (points >= 0),
    CONSTRAINT status_points CHECK ((driver_status <> 'finished') OR (driver_status = 'finished' AND points IS NOT NULL)),
    PRIMARY KEY (driver_id, race_id),
    FOREIGN KEY (driver_id) REFERENCES driver(id) ON DELETE CASCADE,
    FOREIGN KEY (race_id) REFERENCES race(id) ON DELETE CASCADE
);

